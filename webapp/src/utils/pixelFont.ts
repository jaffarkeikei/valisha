/**
 * Generate a bead grid for a name/text
 * @param text - The text to render
 * @param textColor - Color for the letters
 * @param bgColor - Background color
 * @param totalRows - Total rows (default 11)
 * @param letterSpacing - Space between letters in beads
 */
export function generateTextGrid(
  text: string,
  textColor: string,
  bgColor: string,
  totalRows: number = 11,
  letterSpacing: number = 1
): { grid: string[][], rows: number, cols: number } {
  const chars = text.toUpperCase().split('')
  const charWidth = 5
  const fontHeight = 7
  
  // Calculate padding for centering vertically
  const topPadding = Math.floor((totalRows - fontHeight) / 2)
  // bottomPadding would be: totalRows - fontHeight - topPadding (unused)
  
  // Calculate total width
  const totalCols = chars.length * charWidth + (chars.length - 1) * letterSpacing + 2 // +2 for side padding
  
  // Initialize grid with background
  const grid: string[][] = Array(totalRows).fill(null).map(() => 
    Array(totalCols).fill(bgColor)
  )
  
  // Render each character
  let xOffset = 1 // Start with 1 bead padding
  
  for (const char of chars) {
    const charPattern = PIXEL_FONT[char] || DEFAULT_CHAR
    
    // Render the character
    for (let row = 0; row < fontHeight; row++) {
      for (let col = 0; col < charWidth; col++) {
        if (charPattern[row][col] === 1) {
          const gridRow = topPadding + row
          const gridCol = xOffset + col
          if (gridRow < totalRows && gridCol < totalCols) {
            grid[gridRow][gridCol] = textColor
          }
        }
      }
    }
    
    xOffset += charWidth + letterSpacing
  }
  
  return { grid, rows: totalRows, cols: totalCols }
}

